#include <stdio.h>

// Function to add two numbers
int add(int a, int b) {
    return a + b;
}

// Function to subtract two numbers
int sub(int a, int b) {
    return a - b;
}

// Function to multiply two numbers
int mul(int a, int b) {
    return a * b;
}

// Function to divide two numbers (assuming b is not zero)
int div(int a, int b) {
    return a / b;
}

// Function to find the modulo between two numbers
int mod(int a, int b) {
    return (a % b);
}
